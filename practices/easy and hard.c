#include<stdio.h>
int main()
{
    int n,r, ans=0;
    scanf("%d",&n);
    for (int i=0; i<n; i++)
    {
        scanf("%d",&r);
        if (r==1)
        {
            ans==1;
        }
    }
    if (ans==1)
    {
        printf("HARD");
    }else
    {
        printf("EASY");
    }
    return 0;
}
